#dictionary
people= {
    "name": "Fanny",
    "surname":"Mayorga",
    "age" : 19
}

#itera dentro del diccionario
for person in people:
    print(people[person])

#imprime solo las clases de los diccionarios
print(people.keys())

#imprime solo los valores de los diccionarios
